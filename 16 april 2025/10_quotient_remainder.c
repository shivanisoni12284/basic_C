// Write a C program to return the quotient and remainder of a division.
// Test Data and Expected Output :
// Input numerator : 2000                                                 
//  Input denominator : 235                                                
//  quotient =  8, remainder = 120

#include<stdio.h>
int main()
{
    int num=2000,den=235;
    printf("quotient = %d, ",num/den);
    printf("remainder = %d",num%den);
    return 0;
}

    

